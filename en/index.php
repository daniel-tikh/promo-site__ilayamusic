          <h1 class="en_content__title blanco">ИЛАЙЯ</h1>
          
          <p class="en_content__about__text__text">I was born in Nizhny Novgorod, my father graduated from technical college and worked as an engineer, my mother played in a symphony orchestra, but she was educated as a tailor and worked in a military clothing studio which she was forced to leave due to pregnancy. Life was difficult with minimal income and a one-room apartment allocated to my father from work. My mother had to get a job as a teacher in a kindergarten in order to be allocated a place for me. The neighbourhood was industrial and marginal like a distant castle or any periphery of almost any region in which the majority leads the very way that if it does not completely destroy you then does not involve distant attacks from the comfort zone.</p>

          <p class="en_content__about__text__quote">From a conscious age I tried to get out of such a life. I was drawn to people from the field of art and I was in circles of snobs at least.</p>
          
          <p class="en_content__about__text__text">I deeply experienced and still experience any difficulties of any destinies, I have always been empathic and impressionable rather reserved and modest in everyday life and at the same time I could be wildly delighted with everyone's attention during my creative performances. So, my self-determination happened instantly. I understood that all the hardships of life, the pains and sufferings that my loved ones and myself face is my comfortable environment and my way is to absorb, digest and periodically spit it all out in a form that is convenient for me.<br><br>I began to reach for art since my birthday. My parents say I began to sing at the age of three and I wrote poetry for as long as I can remember myself. The first cassettes from the family collection that I listened to were Kino, Nautilus and Zemfira. In general, everything turned out in such a way that the heavy guitar sound became the leitmotif of my whole life. I was impressed by the honesty of such music.</p>
        
