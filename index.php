          <p class="en_content__about__text__text">Я родился в&nbsp;Нижнем Новгороде, отец закончил индустриальный техникум и&nbsp;работал инженером, мама играла в&nbsp;симфоническом оркестре, но&nbsp;получила образование портной и&nbsp;работала в&nbsp;военном ателье, которое&nbsp;была вынуждена оставить в&nbsp;связи с&nbsp;беременностью мной. Жизнь была сложной, минимальный достаток и&nbsp;однокомнатная коммунальная квартира выделенная отцу от&nbsp;производства, матери пришлось устроиться на&nbsp;работу воспитателем в&nbsp;детском саду, для&nbsp;того&nbsp;чтобы&nbsp;на&nbsp;меня&nbsp;выделили место. Район промышленный и&nbsp;маргинальный, как&nbsp;далекое замкадье или&nbsp;любая периферия почти любого региона, в&nbsp;которой&nbsp;большинство ведет тот&nbsp;самый&nbsp;уклад, который&nbsp;если и&nbsp;не&nbsp;уничтожает тебя&nbsp;совсем, то&nbsp;не&nbsp;предполагает дальних вылазок из&nbsp;зоны комфорта.</p>

          <p class="en_content__about__text__quote">С&nbsp;сознательного возраста я&nbsp;стремился выбраться из&nbsp;такой&nbsp;жизни, тянулся к&nbsp;людям из&nbsp;сферы искусства, и&nbsp;водился в&nbsp;кругах снобов, как&nbsp;минимум.</p>
          
